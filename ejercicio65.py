'''Inicializar un string con la cadena "mAriA" luego llamar a sus m√©todos upper(), lower() y capitalize(), 
guardar los datos retornados en otros string y mostrarlos por pantalla.'''

nombre=input("Ingrese el nombre 'mAriA':")
mayuscula= nombre.upper()
minuscula= nombre.lower()
capitalizado= nombre.capitalize()

print("Todo en mayuscula:",mayuscula)
print("Todo en minuscula:",minuscula)
print("Primera letra en mayuscula:",capitalizado)